 1. 什么是对象？
    对象时现实生活中的一个具体存在，看得见、摸得着，拿过来就可以直接使用。
 
 2. 什么是类。
    物以类聚人以群分。
    类是对一群具有相同特征或者行为的事物的一个统称，抽象的，不能直接使用。
    如果非要使用类的话呢？只能去找到这一类事物中的一个具体存在，然后使用这个具体存在。
 
